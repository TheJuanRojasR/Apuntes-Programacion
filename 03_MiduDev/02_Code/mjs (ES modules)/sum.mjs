'use strict'

// Sistemas de modulos ES Modules
// Lo unico que tenemos que hacer es agregar 'export' al principio de la funcion que queremos exportar.

export function sum ( a, b ) {
    return a + b;
}

export function sub ( a, b ) {
    return a - b;
}

export function mult ( a, b ) {
    return a * b;
}